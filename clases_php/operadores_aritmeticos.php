<?php 
	$var1 = 8;
	$var2 = 4;
	$var3 = 11;

	//SUMA
	$suma = $var1 + $var2;
	echo 'Suma: '.$suma.'<br>';

	//RESTA
	$resta = $var1 - $var2;
	echo 'Resta: '.$resta.'<br>';

	//MULTIPLICACION
	$multiplicar = $var1 * $var2;
	echo 'Multiplicacion: '.$multiplicar.'<br>';

	//DIVISION
	$division = $var1 / $var2;
	echo 'Division: '.$division.'<br>';

	//REMANENTE
	$remanente = $var3 % $var2;
	echo 'Remanente: '.$remanente.'<br>';

	//Potenciación
	$potenciacion = $var1 ** $var2;
	echo 'Potenciación: '.$potenciacion.'<br>';
?>